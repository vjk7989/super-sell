export function getProductById(id: string): Product | undefined {
  return products.find((product) => product.id === id);
}

export function getSimilarProducts(product: Product, limit = 4): Product[] {
  return products
    .filter((candidate) => candidate.id !== product.id)
    .map((candidate) => {
      const tagOverlap = candidate.tags.filter((tag) => product.tags.includes(tag)).length;
      const score =
        (candidate.category === product.category ? 4 : 0) +
        (candidate.brand === product.brand ? 3 : 0) +
        tagOverlap;
      return { candidate, score };
    })
    .filter((item) => item.score > 0)
    .sort((a, b) => b.score - a.score)
    .slice(0, limit)
    .map((item) => item.candidate);
}
